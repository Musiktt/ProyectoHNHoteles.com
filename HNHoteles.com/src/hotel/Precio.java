package hotel;

/**
 *
 * @author Stward
 */
public class Precio {
    private double monto;

    public Precio() {
    }

    public Precio(double monto) {
        this.monto = monto;
    }

    public double getMonto() {
        return monto;
    }

    public void setMonto(double monto) {
        this.monto = monto;
    }

    @Override
    public String toString() {
        return "Precio{" + "monto=" + monto + '}';
    }
    
    
    
}
